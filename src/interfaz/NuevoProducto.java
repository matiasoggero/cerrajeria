    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String tipo,marca,modelo,cant;
        int cantidad, validacion = 0;
        
        tipo = cmb_tipo.getSelectedItem().toString();
        marca = txt_marca.getText().trim();
        modelo = txt_modelo.getText().trim();
        cant = txt_cantidad.getText().trim();        
        
        if(marca.equals("")){
            validacion++;
        }
        if(modelo.equals("")){
            validacion++;
        }
        if(cant.equals("")){
            validacion++;
        }
        cantidad = Integer.parseInt(cant);
        
        try {
            Connection c = Conectar.DataBase();
            PreparedStatement ps = c.prepareStatement(
                    "select modelo from productos where modelo = '" + modelo + "'");
            ResultSet r = ps.executeQuery();
            
            
            if (r.next()) {
                JOptionPane.showMessageDialog(null, "el modelo ya existe");
            } else {
                if(validacion == 0){
            
            try {
            Connection cn = Conectar.DataBase();
            PreparedStatement pst = cn.prepareStatement(
                    "insert into productos values (?,?,?,?,?,?)");
            
            pst.setInt(1, 0);
            pst.setString(2, tipo);
            pst.setString(3, marca);
            pst.setString(4, modelo);
            pst.setInt(5, cantidad);
            pst.setString(6, user);
            
            pst.executeUpdate();
            cn.close();
            JOptionPane.showMessageDialog(null, "registro exitoso.");
            this.dispose();
            
        } catch (SQLException e) {
            System.err.println("error al agregar el producto " + e);
        }
            
        }else{
            JOptionPane.showMessageDialog(null, "debes completar todos los campos");
        }
            }
            
        } catch (SQLException e) {
            System.err.println("error al conectar a la tabla productos." + e);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

